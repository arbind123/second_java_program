class Default_Value_PrimitiveDataType{
	
static int a;   		//default value zero(0)
static short b;		//default value zero(0)
static long c;		//default value zero(0)
static byte d;		//default value zero(0)
static float e;		//default value zero(0)
static double f;		//default value zero point zero(0.0)
static String g;		//default value null
	public static void main(String args[]){
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
	System.out.println(f);
	System.out.println(g);
	}
}


